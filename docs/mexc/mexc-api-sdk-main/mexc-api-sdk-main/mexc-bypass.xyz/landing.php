    // Дополнительные subtle анимации при загрузке страницы
    document.addEventListener('DOMContentLoaded', () => {
      // Добавляем gentle pulse к важным элементам
      const heroDemo = document.querySelector('.bg-white.dark\\:bg-dark-800');
      if (heroDemo) {
        heroDemo.classList.add('gentle-pulse');

        // Убираем анимацию через 10 секунд
        setTimeout(() => {
          heroDemo.classList.remove('gentle-pulse');
        }, 10000);
      }

      // Улучшенные scroll анимации для новых элементов
      const scrollElements = document.querySelectorAll('.scroll-fade-in');
      const scrollObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            entry.target.classList.add('visible');
            scrollObserver.unobserve(entry.target);
          }
        });
      }, {
        threshold: 0.1,
        rootMargin: '0px 0px -30px 0px'
      });

      scrollElements.forEach(el => scrollObserver.observe(el));

      // Добавляем subtle анимации к основным секциям
      const sections = document.querySelectorAll('section');
      sections.forEach((section, index) => {
        section.style.animationDelay = `${index * 0.1}s`;
      });

      // Плавное появление navigation при загрузке
      const nav = document.querySelector('nav');
      if (nav) {
        nav.style.opacity = '0';
        nav.style.transform = 'translateY(-20px)';
        nav.style.transition = 'all 0.8s cubic-bezier(0.4, 0, 0.2, 1)';

        setTimeout(() => {
          nav.style.opacity = '1';
          nav.style.transform = 'translateY(0)';
        }, 200);
      }

      // Дополнительные эффекты для CTA кнопки
      const ctaButton = document.querySelector('.cta-button');
      if (ctaButton) {
        // Проверяем, видна ли кнопка при загрузке страницы
        const checkCTAVisibility = () => {
          const rect = ctaButton.getBoundingClientRect();
          const isVisible = rect.top < window.innerHeight && rect.bottom > 0;

          if (isVisible && !ctaButton.classList.contains('visible')) {
            // Если кнопка видна сразу при загрузке, показываем её без анимации
            if (rect.top < window.innerHeight * 0.8) {
              ctaButton.classList.add('initial-visible');
            } else {
              ctaButton.classList.add('visible');
            }
          }
        };

        // Проверяем сразу при загрузке
        checkCTAVisibility();

        // Дополнительная проверка после полной загрузки страницы
        window.addEventListener('load', () => {
          checkCTAVisibility();
        });

        // Проверяем при изменении размера окна
        window.addEventListener('resize', () => {
          checkCTAVisibility();
        });

        // Создаем Intersection Observer для CTA кнопки
        const ctaObserver = new IntersectionObserver((entries) => {
          entries.forEach(entry => {
            if (entry.isIntersecting) {
              setTimeout(() => {
                if (!entry.target.classList.contains('initial-visible')) {
                  entry.target.classList.add('visible');
                }
              }, 200); // Небольшая задержка для плавности
            }
          });
        }, {
          threshold: 0.1,
          rootMargin: '0px 0px -50px 0px'
        });

        ctaObserver.observe(ctaButton);

        // Fallback для браузеров без поддержки Intersection Observer
        if (!window.IntersectionObserver) {
          // Простая проверка при скролле
          window.addEventListener('scroll', () => {
            checkCTAVisibility();
          });
        }
        // Добавляем эффект "магнитного притяжения" курсора
        ctaButton.addEventListener('mousemove', function(e) {
          // Отключаем пульсацию при наведении
          this.style.animation = 'none';

          const rect = this.getBoundingClientRect();
          const x = e.clientX - rect.left - rect.width / 2;
          const y = e.clientY - rect.top - rect.height / 2;

          const distance = Math.sqrt(x * x + y * y);
          const maxDistance = 100;

          if (distance < maxDistance) {
            const force = (maxDistance - distance) / maxDistance;
            const moveX = x * force * 0.1;
            const moveY = y * force * 0.1;

            this.style.transform = `translate(${moveX}px, ${moveY}px) scale(1.02)`;
          } else {
            this.style.transform = 'translate(0, 0) scale(1)';
          }
        });

        ctaButton.addEventListener('mouseleave', function() {
          this.style.transform = 'translate(0, 0) scale(1)';
          // Восстанавливаем пульсацию только если кнопка не в hover состоянии
          if (!this.matches(':hover')) {
            this.style.animation = 'cta-pulse 4s ease-in-out infinite';
          }
        });

        // Добавляем эффект "ripple" при клике
        ctaButton.addEventListener('click', function(e) {
          const ripple = document.createElement('span');
          const rect = this.getBoundingClientRect();
          const size = Math.max(rect.width, rect.height);
          const x = e.clientX - rect.left - size / 2;
          const y = e.clientY - rect.top - size / 2;

          ripple.style.width = ripple.style.height = size + 'px';
          ripple.style.left = x + 'px';
          ripple.style.top = y + 'px';
          ripple.style.position = 'absolute';
          ripple.style.borderRadius = '50%';
          ripple.style.background = 'rgba(255, 255, 255, 0.6)';
          ripple.style.transform = 'scale(0)';
          ripple.style.animation = 'ripple 0.6s linear';
          ripple.style.pointerEvents = 'none';
          ripple.style.zIndex = '10';

          this.appendChild(ripple);

          setTimeout(() => {
            ripple.remove();
          }, 600);
        });

        // Добавляем CSS для ripple анимации
        const style = document.createElement('style');
        style.textContent = `
          @keyframes ripple {
          to {
          transform: scale(4);
          opacity: 0;
          }
          }
          `;
        document.head.appendChild(style);
      }
    });
